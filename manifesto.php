      <h2>The Internet Is Broken. We're Going to Fix It.</h2>

      <p>Not broken in the way your Wi-Fi is broken. Broken at the level of power. Broken by design &mdash; by the decisions of a handful of corporations and governments who decided that the most transformative communications technology in human history should be a product, a surveillance apparatus, and a chokepoint for control.</p>

      <p>We refuse to accept that as the end of the story.</p>

      <hr>

      <h3>What We Believe</h3>

      <p><strong>The Internet belongs to everyone.</strong> It was built with public money, on public research, by people who believed in open standards and shared infrastructure. It was not built to be owned. It was not built to be rationed. It was not built to spy on you.</p>

      <p><strong>Access is a right, not a product.</strong> In 2026, millions of people remain unconnected or underserved &mdash; not because the technology doesn't exist, but because there's no profit in serving them. Meanwhile, those who do have access pay monopoly prices for a degraded, surveilled experience. This is a political failure, not a technical one.</p>

      <p><strong>Centralization is fragility.</strong> Every service that runs through a single company's servers is one court order, one acquisition, or one executive decision away from disappearing, changing, or being turned against you. The architecture of the modern web was not designed for resilience &mdash; it was designed for control.</p>

      <p><strong>Privacy is not a premium feature.</strong> The surveillance economy treats your attention, your data, and your behavior as raw materials. You are not the customer. You are the product. This is not inevitable. It is a choice &mdash; a choice that can be unmade.</p>

      <hr>

      <h3>What We're Building</h3>

      <p>LibreNetwork is a community of people building the internet that should have existed all along.</p>

      <p>We build <strong>mesh networks</strong> &mdash; community-owned infrastructure that routes around outages, censorship, and expensive ISPs. When your neighborhood controls its own network, no one can shut it down with a letter. No one can throttle your connection to sell you a faster lane. The network serves the people who built it.</p>

      <p>We fight for <strong>digital rights</strong> &mdash; the right to communicate without surveillance, the right to access information without a corporate gatekeeper deciding what you see, the right to build and run software without permission from a platform owner. We educate, we advocate, and we build tools that make these rights real rather than theoretical.</p>

      <p>We are <strong>open source</strong> by default &mdash; because software you can't read is software you can't trust. Every tool we build, every network we run, every protocol we implement is available for anyone to inspect, improve, and deploy. Openness is not a strategy. It is a principle.</p>

      <hr>

      <h3>Why Now</h3>

      <p>The window for a free and open internet is not permanently open. The decisions being made right now &mdash; about net neutrality, about encryption backdoors, about platform liability, about broadband infrastructure, about AI and content moderation &mdash; will shape what the internet looks like for the next generation.</p>

      <p>The original vision of the internet &mdash; a decentralized, resilient, end-to-end network where every node was equal &mdash; is worth fighting for. The technology to build that network exists. The knowledge to build it is freely available. What has been missing is the will and the community to do it.</p>

      <p>LibreNetwork exists to provide both.</p>

      <hr>

      <h3>Join Us</h3>

      <p>You don't need to be a network engineer. You don't need to be a programmer. You need to believe that the internet can be better, and be willing to help make it so.</p>

      <p>We are builders, writers, organizers, educators, and advocates. We are people who got fed up and decided to do something about it. We are your neighbors, and we are building something for all of us.</p>

      <p><strong>The internet you were promised still exists. We're building it.</strong></p>

