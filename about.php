      <h2>Our History</h2>

      <p>LibreNetwork has its roots in the early 2010s wave of mesh networking enthusiasm that followed the SOPA fight. In 2012, as Congress debated legislation that would have handed corporations the power to censor the internet, communities across the internet started asking the same question: <em>what if we just built something they couldn't shut down?</em></p>

      <p>One of those communities was the Orlando Meshnet Project, a local chapter of a broader global meshnet movement that grew out of discussions on Reddit and similar forums. Around the same time, project director <strong>Travis Newton</strong> was volunteering with <strong>The Free Network Foundation (FNF)</strong> &mdash; a pioneering organization that deployed community wireless networks and advocated for decentralized internet infrastructure.</p>

      <p>The FNF has since become defunct, but the vision it carried forward didn't disappear with it. In the spirit of open source, LibreNetwork was formed as a fork &mdash; a continuation of that work, incorporating the lessons learned from early mesh efforts and building toward something more durable and community-driven.</p>

      <p>We don't have venture capital. We don't have a corporate sponsor. We have people who believe the internet belongs to everyone and are willing to do something about it. Travis announced the project in April 2019, and the community has been building ever since.</p>

